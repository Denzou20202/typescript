var container = document.getElementById("container");
var prevBtn = document.getElementById("prev");
var nextBtn = document.getElementById("next");
var currentPage = 1;
var itemsPerPage = 10;
var totalPages = Math.ceil(coursesArray.length / itemsPerPage);
function renderPage(page) {
    container.innerHTML = "";
    var start = (page - 1) * itemsPerPage;
    var end = start + itemsPerPage;
    var items = coursesArray.slice(start, end);
    for (var _i = 0, items_1 = items; _i < items_1.length; _i++) {
        var course = items_1[_i];
        var div = document.createElement('div');
        div.classList.add('courseName');
        var titleDiv = document.createElement('div');
        titleDiv.classList.add('titleText');
        var h3 = document.createElement('h3');
        h3.classList.add('title');
        h3.innerText = course.title;
        titleDiv.appendChild(h3);
        var infoDiv = document.createElement('div');
        infoDiv.classList.add('textInfo');
        var p = document.createElement('p');
        p.classList.add('info');
        p.innerText = "Month - ".concat(course.monthDuration, "   Hours - ").concat(course.hourDuration);
        infoDiv.appendChild(p);
        div.append(titleDiv, infoDiv);
        container.appendChild(div);
    }
    prevBtn.disabled = page === 1;
    nextBtn.disabled = page === totalPages;
}
prevBtn.addEventListener("click", function () {
    if (currentPage > 1) {
        currentPage--;
        renderPage(currentPage);
    }
});
nextBtn.addEventListener("click", function () {
    if (currentPage < totalPages) {
        currentPage++;
        renderPage(currentPage);
    }
});
renderPage(currentPage);
